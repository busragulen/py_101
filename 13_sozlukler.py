# Sözlüklerin en büyük özelliği anahtar-değer ilişkisidir.
# Sözlükler {} içerisinde tanımlanır. Sözlüklerin pythondaki adı dict şeklindedir.
# Sözlüklerde indis kavramı yoktur. Anahtar kelime veya sayılardan yola çıkılır. Mesela "isim" bir anahtar iken Python onun değeridir.
# Sözlüklere sonradan değer atanamaz.
# Sözlüklere yeni bir anahtar eklemek için şu yöntem kullanılır:

insan["sac"]="siyah"

sozluk = dict() #ile de sözlük oluşturabiliriz.

# Boş sözlük oluşturmak için ='dan sonra {} konması yeterlidir: 

sayilar= {}

# Sözlükteki bir değeri değiştirmek için anahtar ile işlem yapılır: 

insan["isim"]= "Busra" #Yani yeniden atama yapıyoruz.
